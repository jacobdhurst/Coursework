package ControlSystem;

/**
 * enum to define valid light names
 */
public enum LightName {
    N1, // north lane 1
    N2, // north lane 2
    N3, // north lane 3
    E1, // east lane 1
    E2, // east lane 2
    E3, // east lane 3
    S1, // south lane 1
    S2, // south lane 2
    S3, // south lane 3
    W1, // west lane 1
    W2, // west lane 2
    W3, // west lane 3
    CN, // north pedestrian crossing lights
    CE, // east pedestrian crossing lights
    CS, // south pedestrian crossing lights
    CW //  west pedestrian crossing lights
}
